public class SeriesModel {
    // Public field to store the unique identifier for the series
    public String seriesId;
    // Public field to store the title/name of the series
    public String seriesName;
    // Public field to store the age restriction for the series (2-18)
    public String seriesAge;
    // Public field to store the total number of episodes in the series
    public String seriesNumberOfEpisodes;
}
// SeriesModel class - Data model for storing TV series information
// This class serves as a blueprint for creating series objects with specific attributes